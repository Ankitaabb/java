public class forloop {
    public static void main(String[] args){
        System.out.println("enter a number");
        int num = new java.util.Scanner(System.in).nextInt();
        int r = 1;
        for(int i=1 ; i < num; i++)
        {
            r =r*1;
            System.out.println(r);
        }

    }
    
}
